def additionner(a, b):
    
    
    return a + b

def soustraire(a, b):
   
    return a - b

def multiplier(a, b):
   
    return a * b

def diviser(a, b):
    
    if b == 0:
        
        raise ZeroDivisionError("La division par zéro n'est pas autorisée.")
    else:
        return a / b
def puissance(a,b):
    return a ** b    
    

    

